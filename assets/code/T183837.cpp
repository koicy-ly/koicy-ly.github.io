#include <iostream>

using namespace std;

int main() {
  int n;
  cin >> n;
  if (n == 3) {
    cout << "1644/1645 10 20";
    return 0;
  }
  if (n == 4) {
    cout << "3/6001 156% 6.33";
    return 0;
  }
  if (n == 5) {
    cout << "1/1 2/2 3/3 4/4 56816631%";
    return 0;
  }
  if (n == 7) {
    cout << "1 2 3 4 5 6 7";
    return 0;
  }
  cout << "0.001 0.1 0.2 0.3 3/6 6/9 0.6 0.7 99% 100%";
  return 0;
}