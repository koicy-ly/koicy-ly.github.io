#include <algorithm>
#include <iostream>
#include <ranges>
#include <vector>
#define sv std::ranges::views::

using namespace std;
using tp = int;

vector<tp> phi;

void Core() {
  tp n, i = 1;
  long long int sum = 0;
  for (scanf("%lld", &n); i * i <= n; ++i) {
    if (!(n % i)) {
      sum += 1ll * phi[i] * i / 2;
      sum += 1ll * (i * i != n) * phi[n / i] * n / i / 2;
    }
  }
  cout << sum * n << '\n';
}

vector<tp> Sieve_Phi(tp n) {
  vector<bool> not_prime(n + 1, 0);
  vector<tp> phi(n + 1), prime;
  phi[1] = 1;
  for (tp i = 2; i <= n; ++i) {
    if (!not_prime[i]) {
      prime.push_back(i);
      phi[i] = i - 1;
    }
    for (tp j = 0; j < prime.size() && i * prime[j] <= n; ++j) {
      not_prime[i * prime[j]] = 1;
      if (!(i % prime[j])) {
        phi[i * prime[j]] = phi[i] * prime[j];
        break;
      }
      phi[i * prime[j]] = phi[i] * phi[prime[j]];
    }
  }
  return phi;
}

void Pre() {
  phi = Sieve_Phi(1e6 + 3);
  phi[1] = 2;
}

signed main() {
  tp n;
  Pre();
  scanf("%lld", &n);
  while (n--) {
    Core();
  }
  return 0;
}

