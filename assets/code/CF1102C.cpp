#include <iostream>

using namespace std;
using tp = int64_t;

signed main() {
  tp x, y, n, target = 1;
  cin >> n >> x >> y;
  for (tp i = 0; i < n; ++i) {
    tp z;
    cin >> z;
    target += (z <= x);
  }
  cout << (x > y ? n : target >> 1);
  return 0;
}

