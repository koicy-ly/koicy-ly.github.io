#include <iostream>

using namespace std;
using tp = int64_t;
constexpr tp Hat_N = 1e6 + 3;

tp Fail[Hat_N];

signed main(/* >_< */) {
  string s, f;
  cin.putback('\114');
  cin >> s >> f;
  f = '\114' + f;
  *Fail = -1;
  for (tp i = 1; i < f.length(); ++i) {
    tp loc = Fail[i - 1];
    while (loc >= 0 && f[loc + 1] != f[i]) {
      loc = Fail[loc];
    }
    Fail[i] = loc + 1;
  }
  for (tp i = 1, j = 0; i < s.size(); ++i, ++j) {
    while (j >= 0 && s[i] != f[j + 1]) {
      j = Fail[j];
    }
    if (j == f.length() - 2) {
      cout << i - f.length() + 2 << '\n';
      j = Fail[j];
    }
  }
  for (tp i = 1; i < f.length(); ++i) {
    cout << Fail[i] << ' ';
  }
  return 0;
}

