#include <iostream>
#include <vector>

using namespace std;
using tp = unsigned long long;

void Calc(tp n, tp k, bool f) {
  if (!--n) {
    putchar(k ^ f ^ 48);
    return;
  }
  if (k < (1ull << n)) {
    putchar('0' + f);
    Calc(n, k, 0);
  } else {
    putchar('1' - f);
    Calc(n, k - (1ull << n), 1);
  }
}

signed main() {
  tp n, k;
  scanf("%llu%llu", &n, &k);
  Calc(n, k, 0);
  return 0;
}

