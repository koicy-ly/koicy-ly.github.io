#include <iostream>
#include <ranges>
#include <vector>
#define sv std::ranges::views::

using namespace std;
using tp = long long int;
constexpr tp _MOD_ = 1000000007;

signed main() {
  tp n, m, k;
  string a, b;
  cin >> n >> m >> k >> a >> b;
  b = '\0' + b;
  vector f(m + 1, vector<tp>(k + 1, 0)), sum(m + 1, vector<tp>(k + 1, 0));
  f[0][0] = 1;
  for (auto&& a : a) {
    for (tp i = m; i; --i) {
      for (tp j = k; j; --j) {
        sum[i][j] = (a == b[i]) * (sum[i - 1][j] + f[i - 1][j - 1]);
        f[i][j] = (f[i][j] + sum[i][j]) % _MOD_;
      }
    }
  }
  cout << f[m][k];
  return EXIT_SUCCESS;
}

