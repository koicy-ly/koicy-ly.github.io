#include <cstdint>
#include <iostream>
#include <list>
#include <string>

using namespace std;
using tp = int64_t;

signed main() {
  tp n, mc = -114514;
  list<string> res;
  cin >> n;
  while (n--) {
    tp cnt = 0;
    string name, s;
    cin >> name >> s;
    for (tp i = 2; i < s.size(); ++i) {
      if (s[i - 2] == 's' && s[i - 1] == 'o' && s[i] == 's') {
        ++cnt;
      }
    }
    if (cnt > mc) {
      mc = cnt;
      res.clear();
      res.push_back(name);
    } else if (cnt == mc) {
      res.push_back(name);
    }
  }
  for (auto&& i : res) {
    cout << i << ' ';
  }
  cout << '\n' << mc;
  return 0;
}

