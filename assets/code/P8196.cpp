#include <cstdint>
#include <iostream>

using namespace std;
using tp = int64_t;
constexpr tp Hat_N = 103;

tp v[Hat_N];

void Core() {
  tp n, cnt = 0;
  cin >> n;
  for (tp i = 0; i < n; ++i) {
    cin >> v[i];
  }
  for (tp i = 0; i < n; ++i) {
    for (tp j = i; j < n; ++j) {
      for (tp k = j; k < n; ++k) {
        cnt += v[i] + v[j] == v[k];
      }
    }
  }
  cout << cnt << '\n';
}

signed main(/* >_< */) {
  tp __TEST_CASE__;
  cin >> __TEST_CASE__;
  while (__TEST_CASE__--) {
    Core();
  }
  return 0;
}

